class DaySimulationEntry {
  final int dayNumber;
  final int randomDigitForDayType;
  final String dayType;
  final int randomDigitForDemand;
  final int demand;

  DaySimulationEntry({
    required this.dayNumber,
    required this.randomDigitForDayType,
    required this.dayType,
    required this.randomDigitForDemand,
    required this.demand,
  });
}
